function translateText(text) {
  if (typeof text !== 'string') return text;
  
  // –°–Ω–∞—á–∞–ª–∞ –∏—â–µ–º —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
  if (ruToUkDictionary[text]) {
    return ruToUkDictionary[text];
  }
  
  // –ï—Å–ª–∏ —Ç–æ—á–Ω–æ–≥–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è –Ω–µ—Ç, –∏—â–µ–º —á–∞—Å—Ç–∏—á–Ω—ã–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
  for (const [ru, uk] of Object.entries(ruToUkDictionary)) {
    if (text.includes(ru)) {
      text = text.replace(new RegExp(ru, 'g'), uk);
    }
  }
  
  return text;
}

// –†–µ–∫—É—Ä—Å–∏–≤–Ω–∞—è —Ñ—É–Ω–∫—Ü–∏—è –¥–ª—è –ø–µ—Ä–µ–≤–æ–¥–∞ –æ–±—ä–µ–∫—Ç–∞
function translateObject(obj) {
  if (typeof obj === 'string') {
    return translateText(obj);
  }
  
  if (Array.isArray(obj)) {
    return obj.map(translateObject);
  }
  
  if (typeof obj === 'object' && obj !== null) {
    const translated = {};
    for (const [key, value] of Object.entries(obj)) {
      translated[key] = translateObject(value);
    }
    return translated;
  }
  
  return obj;
}

